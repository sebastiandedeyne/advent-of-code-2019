import test from "ava";
import { runProgram } from "./runProgram";

test("1,9,10,3,2,3,11,0,99,30,40,50", t => {
  t.is(
    runProgram("1,9,10,3,2,3,11,0,99,30,40,50"),
    "3500,9,10,70,2,3,11,0,99,30,40,50"
  );
});

test("1,0,0,0,99", t => {
  t.is(runProgram("1,0,0,0,99"), "2,0,0,0,99");
});

test("2,3,0,3,99", t => {
  t.is(runProgram("2,3,0,3,99"), "2,3,0,6,99");
});

test("2,4,4,5,99,0", t => {
  t.is(runProgram("2,4,4,5,99,0"), "2,4,4,5,99,9801");
});

test("1,1,1,4,99,5,6,0,99", t => {
  t.is(runProgram("1,1,1,4,99,5,6,0,99"), "30,1,1,4,2,5,6,0,99");
});
